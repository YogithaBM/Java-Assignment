class Student{
	String name;
	int age;
	String branch;
	double cgpa;
	
	Student(String name,int age,String branch,double cgpa){
		this.name=name;
		this.age=age;
		this.branch=branch;
		this.cgpa=cgpa;
		System.out.println("Name: "+this.name);
		System.out.println("Age: "+this.age);
		System.out.println("Branch: "+this.branch);
		System.out.println("CGPA: "+this.cgpa);
		

		
	}

}